package backend;

public class MyMath {
	public static double sum(int num1, int num2) {
        return num1 + num2;
    }

    public static double sum(byte num1, byte num2) {
        return num1 + num2;
    }

    public static double sum(float num1, float num2) {
        return num1 + num2;
    }
}
